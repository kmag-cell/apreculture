class RegistrationPage extends StatefulWidget {
  const RegistrationPage({Key? key}) : super(key: key);

  static const routeName = '/registration';

  @override
  _RegistrationPageState createState() => _RegistrationPageState();
}

class _RegistrationPageState extends State<RegistrationPage> {
  final _formKey = GlobalKey<FormState>();
  bool checkedValue = false;
  bool checkboxValue = false;

  String? errorMessage;
  String _username = '';
  String _last_name = '';
  String _first_name = '';
  String _email = '';
  String _password = '';
  String _num = "";

  _registerNow() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    String location = prefs.getString('countryValue').toString() +
        ", " +
        prefs.getString('cityValue').toString() +
        ", " +
        prefs.getString('stateValue').toString();
    String _local = location;
    var isvalid = _formKey.currentState!.validate();
    if (!isvalid) {
      return;
    }
    _formKey.currentState!.save();
    bool isregis = await Provider.of<UserState>(
      context,
      listen: false,
    ).registernow(
        _first_name, _last_name, _email, _num, _username, _password, _local);
    if (isregis) {
      print(isregis);
      Navigator.of(context).pushReplacementNamed(LoginScreen.routeName);
    } else {
      showDialog(
          context: context,
          builder: (context) {
            return AlertDialog(
              title: const Text("Quelque chose ne va pas.Réessayer"),
              actions: [
                RaisedButton(
                  onPressed: () {
                    Navigator.of(context).pop();
                  },
                  child: const Text("OK"),
                )
              ],
            );
          });
    }
  }

