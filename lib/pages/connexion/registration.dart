                            decoration: ThemeHelper().textInputDecoration(
                                'Prénom(s)', 'Entrez votre prénom'),
                            validator: (value) {
                              RegExp regex = RegExp(r'^.{3,}$');
                              if (value!.isEmpty) {
                                return ("Le prénom ne peut pas être vide");
                              }
                              if (!regex.hasMatch(value)) {
                                return ("Enter Valid name(Min. 3 Character)");
                              }
                              return null;
                            },
